public class Plat {
    //Attributs
    private String nom;
    private String type;

    //Constructeur
    public Plat(String nom, String type){
        this.nom = nom;
        this.type = type;
    }

    //Getter/Setter
    public String getNom() { return this.nom; }
    public String getType() { return this.type; }


}


